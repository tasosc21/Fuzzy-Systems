clc;
close all;
clear all;
%-----------------------------------------------------------------------
x=0:1:360;
y=linspace(-0.4,1,361);
z=0.5*sin(2*pi*x/360)+0.5*(sin(2*pi*(x+30)/360)).^2;
count=1;
%-----------------------------------------------------------------------
for i=0.3:0.1:0.6
   model(x,y,z,i,count);
   count=count+1;
end
